class Calculator(var a: Int, var b: Int) {

    fun add(): Int{
        return a + b
    }

    fun minus(): Int{
        return a - b
    }

    fun multiple(): Int {
        return a * b
    }

    fun divide(): Int {
        return a / b
    }
}

